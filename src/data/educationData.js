export function getMajorBySlug(slug) {
  return MAJORS.find(m => m.slug === slug) || null;
}

export function getMajorByName(name) {
  if (!name) return null;
  const key = name.toLowerCase().trim();
  return (
    MAJORS.find(m => m.name.toLowerCase() === key) ||
    MAJORS.find(m => m.slug === key.replace(/\s+/g, "-").replace(/[^a-z0-9-]/g, "")) ||
    null
  );
}

